int	ft_sqrt(int nb)
{
	int	x;
	int	res;

	res = 0;
	x = 0;
	while (res != nb && x <= 46342)
	{
		x++;
		res = x * x ;
		if (x == 46342)
			return (0);
	}
	return (x);
}
